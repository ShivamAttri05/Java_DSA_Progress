package Chapter_2;

public class Java_01_Variables {
    public static void main(String[] args) {
        // Declaring Variable
        int a;
        // Assigning Value
        a = 20;

        // Printing it
        System.out.println("The value of a is " + a);

        // There are a lot of Data Types exists in Java
        /*
        Integer (int)
        LongInteger (long) - Write 'L' in the end of the LongInteger variable

        Boolean (boolean) - True/False

        Floating Point (float) - Write 'f' in the end of the floating point variable
        Double (double)

        Character (char)
        String (String)
        */
    }
}
