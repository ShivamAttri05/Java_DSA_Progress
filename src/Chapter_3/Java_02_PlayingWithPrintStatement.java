package Chapter_3;

public class Java_02_PlayingWithPrintStatement {
    public static void main(String[] args) {
        System.out.println("4 + 4");            // 4 + 4
        System.out.println(4 + 4);              // 8
        System.out.println(4 + 4 + "4");        // 84
        System.out.println(4 + "4" + 4);        // 444
        System.out.println("4" + 4 + 4);        // 444
    }
}
